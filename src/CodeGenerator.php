            $this->ind("{$this->className}_main(_main);"),
            $this->ind("tphp_rt_object_free(&_main->_base);"),
            $this->ind("tphp_fn_arr_free(_argv);"),
            $this->ind("return 0;"),
            "}",
        ]);
    }

